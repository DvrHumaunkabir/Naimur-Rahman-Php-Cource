<?php
/*
-------------------------------------------
|      PHP গ্রেডিং সিস্টেম (switch-case)  |
-------------------------------------------
   -এই প্রোগ্রামে আমরা ছাত্রের নম্বর অনুযায়ী গ্রেড বের করবো।
   -এখানে গুরুত্বপূর্ণ বিষয় হলো:
-------------------------------------------
1. switch (true):  
   - সাধারণত switch() এর মধ্যে কোনো ভ্যালু চেক করা হয়।
   - কিন্তু আমরা এখানে true দিয়েছি, কারণ আমরা একাধিক শর্ত (condition) চেক করবো।
   - প্রতিটি case এ আমরা শর্ত (condition) লিখবো যেমন: ($score >= 80)

2. case ($score >= 80):
   - এই কন্ডিশন সত্য (true) হলে এই case চলবে।
   - এরপর echo করে গ্রেড প্রিন্ট করবে এবং break হবে।

3. default:
   - যদি কোনো case মেলে না (যেমন 33 এর নিচে নম্বর হয়), তখন "Failed" দেখাবে।
-------------------------------------------
*/

//  ছাত্রের নম্বর সেট করা
$score = 80;

//  switch-case দিয়ে গ্রেড নির্ধারণ
switch (true) {

    case ($score >= 80):
        // যদি 80 বা তার বেশি হয়
        echo "A+";
        break;

    case ($score >= 70):
        // যদি 70 বা তার বেশি হয়
        echo "A";
        break;

    case ($score >= 60):
        // যদি 60 বা তার বেশি হয়
        echo "A-";
        break;

    case ($score >= 50):
        // যদি 50 বা তার বেশি হয়
        echo "B";
        break;

    case ($score >= 40):
        // যদি 40 বা তার বেশি হয়
        echo "C";
        break;

    case ($score >= 33):
        // যদি 33 বা তার বেশি হয়
        echo "D";
        break;

    default:
        // অন্যসব ক্ষেত্রে (33 এর কম)
        echo "Failed";
}
?>