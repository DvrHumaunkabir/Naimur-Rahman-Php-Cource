<?php
    $n1 = 11;
    $n2 = "10";

    // == (Equal) অপারেটর: দুই ভেরিয়েবলের মান সমান কিনা পরীক্ষা করে।
    // এখানে $n1 = 11 (integer), $n2 = "10" (string)
    // PHP স্বয়ংক্রিয়ভাবে টাইপ কনভার্শন করবে, তাই 11 == 10 হবে false (0)
    $equal = $n1 == $n2;
    echo $equal;  // আউটপুট: "" বা 0 (false এর মান)

    //-------------------------------------------------------------------

    // != (Not Equal) অপারেটর: দুই ভেরিয়েবলের মান সমান না হলে true
    // 11 != 10 তাই true (1)
    $notEqual = $n1 != $n2;
    echo $notEqual; // আউটপুট: 1

     //-------------------------------------------------------------------
     
    // === (Identical) অপারেটর: মান এবং টাইপ দুইই একই কিনা পরীক্ষা করে
    // এখানে $n1 = integer 11, $n2 = string "10" তাই false
    $identical = $n1 === $n2;
    var_dump($identical); // আউটপুট: bool(false)

     //-------------------------------------------------------------------

    // !== (Not Identical) অপারেটর: মান অথবা টাইপ ভিন্ন হলে true
    // এখানে মান ও টাইপ দুইই ভিন্ন, তাই true
    $notIdentical = $n1 !== $n2;
    var_dump($notIdentical); // আউটপুট: bool(true)

     //-------------------------------------------------------------------

    // > (Greater than) অপারেটর: বাম পাশের ভেরিয়েবল ডান পাশের থেকে বড় কিনা
    // 11 > 10, তাই true
    $greater = $n1 > $n2;
    var_dump($greater); // আউটপুট: bool(true)

     //-------------------------------------------------------------------

    // < (Less than) অপারেটর: বাম পাশের ভেরিয়েবল ডান পাশের থেকে ছোট কিনা
    // 11 < 10, তাই false
    $less = $n1 < $n2;
    var_dump($less); // আউটপুট: bool(false)

     //-------------------------------------------------------------------

    // >= (Greater or Equal) অপারেটর: বড় অথবা সমান কিনা পরীক্ষা করে
    // 11 >= 10, তাই true
    $greaterOrEqual = $n1 >= $n2;
    var_dump($greaterOrEqual); // আউটপুট: bool(true)

     //-------------------------------------------------------------------

    // <= (Less or Equal) অপারেটর: ছোট অথবা সমান কিনা পরীক্ষা করে
    // 11 <= 10, তাই false
    $lessOrEqual = $n1 <= $n2;
    var_dump($lessOrEqual); // আউটপুট: bool(false)
?>